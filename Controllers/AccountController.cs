        [HttpPost]
        public ActionResult RegisterUser(RegisterUser reg)
        {
            try
            {
                if (ModelState.IsValid)
                {
                    var checkEmail = db.RegisterUser.Count(model => model.Email == reg.Email);
                    var checkPhone = db.RegisterUser.Count(model => model.Phone == reg.Phone);

                    if (checkEmail > 0)
                    {
                        ViewBag.emailExist = "Email is already registered!";
                        return View();
                    }

                    if (checkPhone > 0)
                    {
                        ViewBag.PhoneExist = "Phone Number is already registered!";
                        return View();
                    }

                    string passwordHash = HashPassword(reg.Password);
                    string sessionId = Guid.NewGuid().ToString();

                    RegisterUser newUser = new RegisterUser()
                    {
                        FirstName = reg.FirstName,
                        LastName = reg.LastName,
                        Email = reg.Email,
                        Phone = reg.Phone,
                        Password = passwordHash,
                        ConfirmPassword = passwordHash,
                        SessionID = sessionId,
                        Role = "User",
                        CreatedAt = DateTime.Now
                    };

                    db.RegisterUser.Add(newUser);
                    db.SaveChanges();

                    ViewBag.Success = "User Registered Successfully...";
                    ModelState.Clear();
                    return View();
                }
            }
            catch (DbEntityValidationException ex)
            {
                foreach (var validationErrors in ex.EntityValidationErrors)
                {
                    foreach (var validationError in validationErrors.ValidationErrors)
                    {
                        ModelState.AddModelError(validationError.PropertyName, validationError.ErrorMessage);
                    }
                }
            }
            catch (Exception ex)
            {
                ModelState.AddModelError("", ex.Message);
            }

            return View(reg);
        }

        private string HashPassword(string password)
        {
            using (SHA256 sha256 = SHA256.Create())
            {
                byte[] bytes = sha256.ComputeHash(Encoding.UTF8.GetBytes(password));
                StringBuilder builder = new StringBuilder();
                for (int i = 0; i < bytes.Length; i++)
                {
                    builder.Append(bytes[i].ToString("x2"));
                }
                return builder.ToString();
            }
        }

